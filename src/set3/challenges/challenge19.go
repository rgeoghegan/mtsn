func guessAtPosition(texts [][]byte, pos int) uint8 {
    var textBuffer bytes.Buffer
    for _, line := range texts {
        if len(line) > pos {
            textBuffer.WriteByte(line[pos])
        }
    }
    text := textBuffer.Bytes()
    return mtsn.SortedSolutions(text)[0]
}

