func Challenge19() {
    longestText := 0
    texts := make([][]byte, len(cleartexts))
    nonce := []byte("\x00\x00\x00\x00\x00\x00\x00\x00")
    key := mtsn.GenerateRandomKey()

    for i, line := range cleartexts {
        text := mtsn.DecodeBase64(line)
        encoded := mtsn.CtrCoding(nonce, key, text)
        texts[i] = encoded

        if len(encoded) > longestText {
            longestText = len(encoded)
        }
    }
    solution := make([]byte, longestText)

    for pos, cheat := range cheats {
        solution[pos] = texts[cheat[0]][pos] ^ cheat[1]
    }
    var found bool

    for i := 0; i < longestText; i++ {
        _, found = cheats[i]
        if found {continue}
        
        solution[i] = guessAtPosition(texts, i)
    }
    var fullText bytes.Buffer

    for _, line := range texts {
        decoded := mtsn.XorBytes([]byte(line), solution)
        fullText.Write(decoded)
    }
    fmt.Printf("Challenge 19: Final text score: %f\n",
        mtsn.ScoreAlphabet(fullText.String()))
}
